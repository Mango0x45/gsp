func EscapeString(s string) string {
	return escape(s, attrchars)
}

func EscapeText(s string) string {
	return escape(s, descchars)
}

func escape(s string, mask [256]bool) string {
	var bob strings.Builder
	bob.Grow(len(s))
	for _, b := range []byte(s) {
		if mask[b] {
			bob.WriteByte('\\')
		}
		bob.WriteByte(b)
	}
	return bob.String()
}
